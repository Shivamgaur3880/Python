# LIST LOOP

lst=[1,2,3,4,"rohan"]

for item in lst:
    print(item)

# DICTIONARY LOOP
new_dict={"name"  : "harry",
          "class" : "4th",
          "marks" : 33.33,
"hours in school" : 6  }


for item in new_dict:
    print(item)

# SETS LOOP

s1={1,1,1,1,1,2,2,2,2,3,4,5,6,6,6,6,7,7,7,"harsh"}

for item in s1:
    print(item)

# TUPLES LOOP

a=("harsh",'rohan',"sonu","monu",24,24,24)

for item in a:
    print(item)